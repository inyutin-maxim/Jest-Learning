export class Calc {
	public sum(left: number, right: number): number {
		return left + right;
	}
	
	public minus(left: number, right: number): number {
		return left - right;
	}
	
	public multiply(left: number, right: number): number {
		return left * right;
	}
	
	public division(left: number, right: number): number {
		return left / right;
	}
}